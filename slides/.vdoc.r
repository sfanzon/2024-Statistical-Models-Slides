# Define the step function
step_function <- function(x) {
  if (x < 0) {
    return(0)
  } else if (x < 1) {
    return(1/8)
  } else if (x < 2) {
    return(1/2)
  } else if (x < 3) {
    return(7/8)
  } else {
    return(1)
  }
}

